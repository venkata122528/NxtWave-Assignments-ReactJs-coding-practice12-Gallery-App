class Gallery extends Component {
  state = {selectedThumbnailId: imagesList[0].id}

  onClickThumbnail = thumbnailId => {
    this.setState({selectedThumbnailId: thumbnailId})
  }

  render() {
    const {selectedThumbnailId} = this.state
    const [selectedThumbnail] = imagesList.filter(
      each => each.id === selectedThumbnailId,
    )

    return (
      <div className="mainContainer">
        <div className="card">
          <img
            src={selectedThumbnail.imageUrl}
            alt={selectedThumbnail.imageAltText}
            className="mainImage"
          />
          <h1 className="mainHeading">Nature Photography</h1>
          <p className="para">Nature Photography by Rahul</p>
          <ul className="thumbnailsContainer">
            {imagesList.map(eachThumbnail => (
              <ThumbnailItem
                eachThumbnail={eachThumbnail}
                key={eachThumbnail.id}
                isSelected={eachThumbnail.id === selectedThumbnail.id}
                onClickThumbnail={this.onClickThumbnail}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
